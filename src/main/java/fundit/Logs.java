package fundit;

public class Logs {
	private int c_id, aadhar_buy, aadhar_sell;
	private double stake, offer;
	private String t_date, t_time;
	
	public int getC_id() {
		return c_id;
	}
	public void setC_id(int c_id) {
		this.c_id = c_id;
	}
	public int getAadhar_buy() {
		return aadhar_buy;
	}
	public void setAadhar_buy(int aadhar_buy) {
		this.aadhar_buy = aadhar_buy;
	}
	public int getAadhar_sell() {
		return aadhar_sell;
	}
	public void setAadhar_sell(int aadhar_sell) {
		this.aadhar_sell = aadhar_sell;
	}
	public double getStake() {
		return stake;
	}
	public void setStake(double stake) {
		this.stake = stake;
	}
	public double getOffer() {
		return offer;
	}
	public void setOffer(double offer) {
		this.offer = offer;
	}
	public String getT_date() {
		return t_date;
	}
	public void setT_date(String t_date) {
		this.t_date = t_date;
	}
	public String getT_time() {
		return t_time;
	}
	public void setT_time(String t_time) {
		this.t_time = t_time;
	}
}
